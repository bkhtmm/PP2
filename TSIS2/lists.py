fruits = ["apple", "banana", "cherry"]
print(fruits)

fruits.append("orange")
print(fruits)

fruits.remove("banana")
print(fruits)

fruits.insert(1, "grape")
print(fruits)

fruits.extend(["kiwi", "mango"])
print(fruits)

print(fruits[1])
print(fruits[-1])

fruits.pop()
print(fruits)

fruits.sort()
print(fruits)

fruits.reverse()
print(fruits)
